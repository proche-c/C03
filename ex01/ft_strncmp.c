int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned char	a;
	unsigned char	b;

	while (*s1 != '\0' && *s2 != '\0' && n > 0)
	{
		if (*s1 != *s2)
			break ;
		s1++;
		s2++;
		n--;
	}
	a = (unsigned char)*s1;
	b = (unsigned char)*s2;
	if (n == 0)
		return (0);
	else
		return (a - b);
}
